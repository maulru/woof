package com.example.igorg.woof.modelo;

public class ONG {
    String Nome;
    String id;
    String URL_foto;
    String telefone;
    String endereco;
    String instagram;

    public ONG() {
    }

    public String getNome() {
        return Nome;
    }

    public String getId() {
        return id;
    }


    public String getTelefone() {
        return telefone;
    }


    public String getEndereco() {
        return endereco;
    }


    public String getInstagram() {
        return instagram;
    }
    public String getImagem() {
        return URL_foto;
    }

}
